    public DetallesDeportivo(String name, EventosMetodos EventoFuncion, String codigoEvento) {
        usuariosArray = Login.getUsuariosArray();
        this.name = name;
        this.codigoEvento = codigoEvento;
        funcionEvento = EventoFuncion != null ? EventoFuncion : new EventosMetodos();

        initComponents();

        for (int indice = 0; indice < usuariosArray.size(); indice++) {
            if (usuariosArray.get(indice).getUsuario().equals(name)) {
                ArrayList<Evento> eventos = usuariosArray.get(indice).getListaEventos();
                for (Evento evento : eventos) {
                    if (evento.getCodigo().equals(codigoEvento)) {
                        EventoDeportivo deportivoEvento = (EventoDeportivo) evento;
                        Equipo1Textbox.setText(((EventoDeportivo) evento).getEquipo1());
                        Equipo2Textbox.setText(((EventoDeportivo) evento).getEquipo2());
                        tipoDeporte = ((EventoDeportivo) evento).getTipoDeporte().toString();
                        jComboBox1.setSelectedItem(((EventoDeportivo) evento).getTipoDeporte());
                    }
                }
            }

        }
    }

