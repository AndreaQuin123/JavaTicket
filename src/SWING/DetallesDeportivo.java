        for (int indice = 0; indice < usuariosArray.size(); indice++) {

            if (usuariosArray.get(indice).getUsuario().equals(name)) {

                ArrayList<Evento> eventos = usuariosArray.get(indice).getListaEventos();
                for (Evento evento : eventos) {
                    if (evento.getCodigo().equals(codigoEvento)) {
                        deportivoEvento = (EventoDeportivo) evento;
                    }
                }
            }
        }

        ArrayList<String> jugadores1 = new ArrayList<>();
        ArrayList<String> jugadores2 = new ArrayList<>();

        String nuevoEquipo1 = Equipo1Textbox.getText();

        String nuevoEquipo2 = Equipo2Textbox.getText();

